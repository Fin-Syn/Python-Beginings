# The user input word slicing program
name = input("Print Your Name, Fist Middle and Last\n")
print ("This is your name?")
print (name)
number_1 = eval(input("Enter a 1 digit number\n"))
number_2 = eval(input("Enter a second 1 digit number\n"))
print ("Now we are going to slice up that name")
print (name [number_1:number_2] )
